gdt_start:
  null_descriptor:
    dq 0x0

  kernel_code:
    db 0xFF, 0xFF, 0x00, 0x00
    db 0x00, 0x9A, 0xCF, 0x00

  kernel_data:
    db 0xFF, 0xFF, 0x00, 0x00
    db 0x00, 0x92, 0xCF, 0x00

gdt_end:

gdt_descriptor:
  dw gdt_end - gdt_start - 1
  dd gdt_start

; code_seg = 0x08
; data_seg = 0x10
